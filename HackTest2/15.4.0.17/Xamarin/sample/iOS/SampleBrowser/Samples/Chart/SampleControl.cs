namespace SampleBrowser
{
	public class SampleControl : SampleView
	{
		public SampleControl ()
		{
		}
			
		public override void LayoutSubviews ()
		{
			foreach (var view in this.Subviews) {
				view.Frame = Frame;
			}
			base.LayoutSubviews ();
		}
	}
}

